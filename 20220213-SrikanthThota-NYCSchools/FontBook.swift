import Foundation
import UIKit


public struct FontBook {
    public static var title: UIFont {
        return UIFont.boldSystemFont(ofSize: 16)
    }
    
    public static var labels: UIFont {
        return UIFont.systemFont(ofSize: 14)
    }
}
